nome = "Felipe Rodrigues Gonçalves"

print(nome[0]) #primeira letra
print(nome[-1]) #última letra
print(nome[:9]) #exibe a nona letra
print(nome[6:]) #retira as 6 primeiras letras
print(nome[10:16]) #intervalo da string
print(nome[6:16:3]) #pega um intervalo e pula no caso de 3 em 3 a string para exibir a letra
print(nome[:]) #exibe a string completa
print(nome[::-1]) #espelha a string - inverte a sequencia de letras
                                                         